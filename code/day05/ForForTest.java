/*
嵌套循环的使用
1.嵌套循环:将一个循环结构A声明在另一个循环结构B的循环体中，就构成了嵌套循环
2.
外层循环:循环结构B
内层循环:循环结构A

3.说明
①内层循环结构遍历一遍，只相当于外层循环循环体执行了一次
D假设外层循环需要执行m次，内层循环需要执行n次。此时内层循环的循环体一共执行了m*n次

*/


class ForForTest 
{
	public static void main(String[] args) 
	{
		//******
		//System.out.println("******");
		for (int i = 0;i < 6 ;i++ )
		{
			System.out.print('*');
		}

		/*
		*****
		*****
		*****
		*****
		*/
		for (int i = 1;i < 5 ;i++ )
		{
			System.out.println();
			for (int j = 0;j < 7 ;j++ )
			{
				System.out.print('*');
			}
		}
		System.out.println("\n");

		/*
		*
		**
		***
		****
		*****
		*/
		for (int i = 0;i < 5 ;i ++ )
		{
			for (int j = 0;j <= i ;j++ )
			{
				System.out.print('*');
			}
			System.out.println();
		}


		/*				i(行数)		j(*的个数)			规律：i + j =6  换句话说：j = 6 - i
		*****			1			5
		****			2			4
		***				3			3
		**				4			2
		*				5			1
		*/
		for (int i = 0;i < 5 ;i++ )
		{
			for (int j = 0 ;j < 5 - i ;j++ )
			{
				System.out.print('*');
			}
			System.out.println();
		}
		System.out.println("\n");

		/*					i(行数)			j(- 的个数)			k(* 的个数)		j = 5 - i		k = i
		    *				1				4					1
		   * *				2				3					2
		  * * *				3				2					3
		 * * * *			4				1					4
		* * * * *			5				0					5
		 * * * *			1				1					4
		  * * *				2				2					3
		   * *				3				3					2
		    *				4				4					1				j = i		k = 5 - i
		*/
		
		//上半部分
		for (int i = 1;i <= 5 ;i++ )
		{
			for (int j = 1 ;j <= 5 - i ;j++ )
			{
				System.out.print(" ");
			}
			for (int k = 1 ;k <= i  ;k++ )
				{
					System.out.print('*');
					System.out.print(" ");
				}
			System.out.println();
		}


		//下半部分
		for (int i = 1;i <= 4 ;i++ )
		{
			for (int j = 1;j <= i ;j++ )
			{
				System.out.print(" ");
			}
			for (int k = 1;k <= 5 - i ;k++ )
			{
				System.out.print("* ");
			}
			System.out.println();
		}

	}
}
